    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            // TODO add your handling code here:
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/datamahasiswa","root","");
            conn.createStatement().executeUpdate("insert into mahasiswa values"
                    +"('"+nimku.getText()+"','"+namaMahasiswa.getText()+"','"+prodiMahasiswa.getText()
                    +"','"+fakultasMahasiswa.getText()+"')");
            tampilkan();
            nimku.setText("");
            namaMahasiswa.setText("");
            prodiMahasiswa.setText("");
            fakultasMahasiswa.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ada data yang belum diisi");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // TODO add your handling code here:
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/datamahasiswa","root","");
            conn.createStatement().executeUpdate("update mahasiswa set nama ='"+namaMahasiswa.getText()
                    +"',prodi ='"+prodiMahasiswa.getText()+"',fakultas ='"+fakultasMahasiswa.getText()
                    +"' where nim='"+nimku.getText()+"'");
            tampilkan();
            nimku.setText("");
            namaMahasiswa.setText("");
            prodiMahasiswa.setText("");
            fakultasMahasiswa.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void TabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelMouseClicked
        // TODO add your handling code here:
        int i = Tabel.getSelectedRow();
        
        if(i>-1){
            nimku.setText(model.getValueAt(i, 0).toString());
            namaMahasiswa.setText(model.getValueAt(i, 1).toString());
            prodiMahasiswa.setText(model.getValueAt(i, 2).toString());
            fakultasMahasiswa.setText(model.getValueAt(i, 3).toString());
            
        }
    }//GEN-LAST:event_TabelMouseClicked

