    private void tampilkan() {
        int row = Tabel.getRowCount();
        for(int a=0; a<row; a++){
            model.removeRow(0);
        }
        try {
            // Pengkoneksian ke database mysql
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/datamahasiswa","root","");
            ResultSet res = conn.createStatement().executeQuery("SELECT * FROM mahasiswa");
            while(res.next()){
                int dataNim  = res.getInt(1);
                
                String data [] = {res.getString(2),res.getString(3),res.getString(4)};
                model.addRow(new Object[]{
                    dataNim,
                    data[0],
                    data[1],
                    data[2]
                });
                
            } 
        }   catch (SQLException ex) {
            Logger.getLogger(Mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
